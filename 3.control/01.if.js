// 조건문 (Conditional Statement)
// if (조건) {}
// if (조건) {} else {}
// if (조건1) {} if else (조건2) {} else {}

let fruit = 'orange';
if (fruit === 'apple') {
  console.log('🍎');
} else if (fruit === 'orange') {
  console.log('🍊');
} else {
  console.log('!!');
} // 🍊

