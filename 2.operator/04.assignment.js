// 할당 연산자 (Assignment Operators)

let a = 1; // 할당
a = a + 2; // 재할당

console.log(a); // 3

a += 2; // a = a + 2; 축약 버전
console.log(a); // 5

a -= 2; // a = a - 2;
console.log(a); // 3

a *= 2; // a = a * 2;
console.log(a); // 6

a /= 2; // a = a / 2
console.log(a); // 3

a %= 2; // a = a % 2
console.log(a); // 1

a **= 2; // a = a * a
console.log(a); // 1
